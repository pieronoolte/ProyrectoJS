class Cliente{
    nombre;
    email;
    telefono;
    distrito;
    direccion;
    referencia;

    constructor(nombre, email, telefono, distrito, direccion, referencia) {
        this.nombre = nombre;
        this.email = email;
        this.telefono = telefono;
        this.distrito = distrito;
        this.direccion = direccion;
        this.referencia = referencia;
    }     

}


